
package com.doublechaintech.lsc;


public class LscRuntimeException extends RuntimeException {
    static final long serialVersionUID = -1;

    public LscRuntimeException() {
        super();
    }


    public LscRuntimeException(String message) {
        super(message);
    }


    public LscRuntimeException(String message, Throwable cause) {
        super(message, cause);
    }


    public LscRuntimeException(Throwable cause) {
        super(cause);
    }


   
}












